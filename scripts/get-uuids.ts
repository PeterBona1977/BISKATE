
import { createClient } from '@supabase/supabase-js'
import dotenv from 'dotenv'
import path from 'path'
import fs from 'fs'

dotenv.config({ path: path.resolve(process.cwd(), '.env.local') })

const supabase = createClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!
)

async function run() {
    const { data: req } = await supabase.from('emergency_requests').select('service_id').order('created_at', { ascending: false }).limit(1)
    const { data: p } = await supabase.from('profiles').select('skills').eq('email', 'pmbonanca@gmail.com').single()

    const output = {
        req_service_id: req?.[0]?.service_id,
        prov_skills: p?.skills
    }

    fs.writeFileSync('debug_uuids.json', JSON.stringify(output, null, 2))
    console.log("Wrote debug_uuids.json")
}

run()
